package by.trjava.kaloshych.service.exception;

public class AdditionalIngredientServiceException extends Exception {
    public AdditionalIngredientServiceException() {
    }

    public AdditionalIngredientServiceException(String message) {
        super(message);
    }

    public AdditionalIngredientServiceException(String message, Throwable cause) {
        super(message, cause);
    }

    public AdditionalIngredientServiceException(Throwable cause) {
        super(cause);
    }
}
